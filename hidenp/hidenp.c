#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;
	int	len;

	i = 0;
	j = 0;
	if (argc == 3)
	{
		while(argv[1][i])
			i++;
		len = i;
		i = 0;
		while (argv[1][i] && argv[2][j])
		{
			if (argv[1][i] == argv[2][j])
				i++;
			j++;
		}
		if (i == len)
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
	return (0);
}
