#include <unistd.h>

int		max(int* tab, unsigned int len)
{
	unsigned int	i;
	int	max;

	i = 0;
	max = -2147483647;
	while (i < len)
	{
		if (tab[i] > max)
			max= tab[i];
		i++;
	}
	return (max);
}
